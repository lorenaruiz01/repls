function add(num1, num2) {
  sum = (num1 + num2);
  return sum;
}

function subtract(num1, num2){
  difference = (num2 - num1);
  return difference;
}

function multiply(num1, num2){
  product = (num1 * num2);
  return product;
}

function divide(num1, num2){
  if (num2 === 0){
    return'Not Allowed!';
  } else
  quotient = (num1 / num2)
  return quotient;
} 

